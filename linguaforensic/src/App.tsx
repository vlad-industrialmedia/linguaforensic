const getDynamicCategoryGroup = (indicatorKey: string, res: any, backendCat: any) => {
  if (!res) {
    const staticItem = UNIFIED_CATEGORIES.find(c => c.indicatorKey === indicatorKey);
    return staticItem?.group || "good";
  }

  const backendScore = backendCat?.score;
  const isBackendAI = backendScore === "AI" || backendScore === "ШІ";
  const isBackendHuman = backendScore === "Человек" || backendScore === "Людина";

  const indicators = res.indicators || {};

  switch (indicatorKey) {
    case "entropy": {
      const v = indicators.entropy;
      if (v === undefined || v === null) return "good";
      if (v > 5.2) return "fix";
      if (v > 4.8) return "caution";
      return "good";
    }
    case "structural": {
      const len = res.structuralPatterns?.length ?? 0;
      if (len > 2) return "fix";
      if (len > 0) return "caution";
      return "good";
    }
    case "ttr": {
      const v = indicators.ttr;
      if (v === undefined || v === null) return "good";
      if (v > 0.78 || v < 0.45) return "fix";
      if (v < 0.55 || (v >= 0.75 && v <= 0.78)) return "caution";
      return "good";
    }
    case "cvSentenceLength": {
      const v = indicators.cvSentenceLength;
      if (v === undefined || v === null) return "good";
      if (v < 0.35) return "fix";
      if (v < 0.50) return "caution";
      return "good";
    }
    case "lexicalDensity": {
      let v = indicators.lexicalDensity;
      if (v === undefined || v === null) return "good";
      if (v <= 1) v = v * 100;
      if (v < 45 || v > 75) return "fix";
      if (v < 55 || v > 68) return "caution";
      return "good";
    }
    case "dependencyDepth": {
      const v = indicators.dependencyDepth;
      if (v === undefined || v === null) return "good";
      if (v < 2.5) return "fix";
      if (v < 3.5) return "caution";
      return "good";
    }
    case "fleschEase": {
      const v = indicators.fleschEase;
      if (v === undefined || v === null) return "good";
      if (v < 10 || v > 95) return "fix";
      if (v < 25 || v > 85) return "caution";
      return "good";
    }
    case "psycholinguistic": {
      if (isBackendAI) return "fix";
      if (isBackendHuman) return "good";
      return "caution";
    }
    case "hapaxLegomena": {
      let v = indicators.hapaxLegomena;
      if (v === undefined || v === null) return "good";
      if (v <= 1) v = v * 100;
      if (v < 3.0) return "fix";
      if (v < 5.0) return "caution";
      return "good";
    }
    case "hedgesCount": {
      const v = indicators.hedgesCount;
      if (v === undefined || v === null) return "good";
      if (v === 0 || v > 10) return "fix";
      if (v < 2 || v > 6) return "caution";
      return "good";
    }
    case "valenceRange": {
      const v = indicators.valenceRange;
      if (v === undefined || v === null) return "good";
      if (v < 0.25) return "fix";
      if (v < 0.40) return "caution";
      return "good";
    }
    case "morphology": {
      if (isBackendAI) return "fix";
      if (isBackendHuman) return "good";
      return "caution";
    }
    default:
      return "good";
  }
};

